	static void search(int[] a, int x) {
		int l = 0, h = a.length - 1, mid = 0;
		while(l <= h) {
			int m = l + (h-l)/2;
			if(a[m] == x) {
				if(high == -1)
					high = m;
				low = m;
				h = m - 1;
			} else if(a[m] > x) {
				h = m-1;
			} else  {
				l = m + 1;
			}
		}

		if(low != -1) {
			l = high; h = a.length - 1; mid = 0;
			while(l <= h) {
				int m = l + (h-l)/2;
				if(a[m] == x) {
					high = m;
					l = m + 1;
				} else if(a[m] > x) {
					h = m-1;
				} else  {
					l = m + 1;
				}
			}

		}

	}

